'''
Script en python que calcula elarea de un triangulo.El suuario debera ingresar tanto el valor de la base y la altura
luego el programa mostrara el valordel area..
'''
base=int(input("Ingrese la base del triangulo: "))
altura=int(input("Ingrese la altura triangulo: "))
area=base*altura/2
print(f"El area del triangulo es {area}")
